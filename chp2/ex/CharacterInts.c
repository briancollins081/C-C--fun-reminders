/**
 * program that prints the integer equivalents of some uppercase letters, lowercase letters,
 * digits and special symbols. As a minimum, determine the integer equivalents of the following:
 * A B C a b c 0 1 2 $ * + / and the blank character.
 */
#include <stdio.h>
int main(void){
    printf("'A' - %d\n", 'A');
    printf("'B' - %d\n", 'B');
    printf("'C' - %d\n", 'C');
    printf("'a' - %d\n", 'a');
    printf("'b' - %d\n", 'b');
    printf("'c' - %d\n", 'c');
    printf("'0' - %d\n", '0');
    printf("'1' - %d\n", '1');
    printf("'2' - %d\n", '2');
    printf("'$' - %d\n", '$');
    printf("'*' - %d\n", '*');
    printf("'+' - %d\n", '+');
    printf("'/' - %d\n", '/');
    printf("' ' - %d\n", ' ');
}